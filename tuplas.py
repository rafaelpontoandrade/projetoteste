#Tuplas são listas imutáveis
#Para criar uma tupla, basta ao invés de usar  []  para setar o valor, usar ()


valores = (1,2,3,4)
print(valores)

#dessa forma eu não consigo modificar os valores

# Quando uma tupla possui apenas um item, devemos adicionar uma vírgula após o elemento para que o Python reconheça o objeto como tupla. Caso contrário, é obtido um objeto do tipo String.
valor_tupla = (1,)
print(valor_tupla)


